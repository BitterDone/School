    /**
     * Allows for the setting of the directional buttons
     * @param e - the event
     */
    public void keyPressed( KeyEvent e )
    {
        int x = e.getKeyCode();

        if( buttonFocus[0] )
        {
            canvas.setP1Up( x );
            P1Up.setText( "" + ( char ) x );
            buttonFocus[0] = false;
        }
        else if( buttonFocus[1] )
        {
            canvas.setP1Down( x );
            P1Down.setText( "" + ( char ) x );
            buttonFocus[1] = false;
        }
        else if( buttonFocus[2] )
        {
            canvas.setP2Up( x );
            P2Up.setText( "" + ( char ) x );
            buttonFocus[2] = false;
        }
        else if( buttonFocus[3] )
        {
            canvas.setP2Down( x );
            P2Down.setText( "" + ( char ) x );
            buttonFocus[3] = false;
        }
    }
