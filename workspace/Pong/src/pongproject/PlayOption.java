    /**
     * Creates new form PlayOption
     * @param canvas - the canvas of the program
     */
    public PlayOption( PongCanvas canvas )
    {
        this.canvas = canvas;
        initComponents();
        if( canvas.isComputer() )
        {
            Computer.setText( "Computer" );
        }
        else
        {
            Computer.setText( "Two Player" );
        }

        if( canvas.getSeizureMode() )
        {
            seizureMode.setText( "Enabled" );
        }
        else
        {
            seizureMode.setText( "Disabled" );
        }

        BackgroundColor.setBackground( canvas.getBackgroundColor() );

        if( canvas.getCompDifficulty() == 1 )
        {
            buttonGroup1.setSelected( Easy.getModel(), true );
        }
        else if( canvas.getCompDifficulty() == 2 )
        {
            buttonGroup1.setSelected( Moderate.getModel(), true );
        }
        else if( canvas.getCompDifficulty() > 4 || canvas.getCompDifficulty() < 1 )
        {
            buttonGroup1.setSelected( Normal.getModel(), true );
        }
        else if( canvas.getCompDifficulty() == 3 )
        {
            buttonGroup1.setSelected( Hard.getModel(), true );
        }
        else if( canvas.getCompDifficulty() == 4 )
        {
            buttonGroup1.setSelected( Hardcore.getModel(), true );
        }
        this.setLocation( canvas.getX(), canvas.getY() );

    }

    public void changeBackgroundColor( Color color )
    {
        BackgroundColor.setBackground( color );
    }

