    private void ComputerActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_ComputerActionPerformed
    {//GEN-HEADEREND:event_ComputerActionPerformed
        // TODO add your handling code here:
        if( canvas.isComputer() )
        {
            canvas.setTwoPlayer( false );
            Computer.setText( "Two Player" );
        }
        else
        {
            canvas.setTwoPlayer( true );
            Computer.setText( "Computer " );
        }
    }//GEN-LAST:event_ComputerActionPerformed

    /**
     * Finalizes the options
     * @param evt - the event
     */
    private void OKActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_OKActionPerformed
    {//GEN-HEADEREND:event_OKActionPerformed
        // TODO add your handling code here:
        if( buttonGroup1.isSelected( Easy.getModel() ) )
        {
            canvas.setComputerDifficulty( 1 );
        }
        else if( buttonGroup1.isSelected( Moderate.getModel() ) )
        {
            canvas.setComputerDifficulty( 2 );
        }
        else if( buttonGroup1.isSelected( Normal.getModel() ) )
        {
            canvas.setComputerDifficulty( 5 );
        }
        else if( buttonGroup1.isSelected( Hard.getModel() ) )
        {
            canvas.setComputerDifficulty( 3 );
        }
        else if( buttonGroup1.isSelected( Hardcore.getModel() ) )
        {
            canvas.setComputerDifficulty( 4 );
        }


        this.setVisible( false );
    }//GEN-LAST:event_OKActionPerformed

