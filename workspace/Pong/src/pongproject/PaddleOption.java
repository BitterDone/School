    /**
     * Finalizes the options
     * @param evt - the event
     */
    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
        int sizeOne;
        int sizeTwo;
        int speedOne;
        int speedTwo;

        try
        {
            sizeOne = Integer.parseInt( jTextFieldSizeOne.getText() );
            if( sizeOne > 80 )
                sizeOne = 80;
            canvas.setPad1Size( sizeOne );
        }
        catch( Exception e )
        {
            if( jTextFieldSizeOne.getText() != null )
                System.out.println( "Pad One size invalid." );
        }

        try
        {
            sizeTwo = Integer.parseInt( jTextFieldSizeTwo.getText() );
            if( sizeTwo > 80 )
                sizeTwo = 80;

            canvas.setPad2Size( sizeTwo );
        }
        catch( Exception e )
        {
            if( jTextFieldSizeTwo.getText() != null )
                System.out.println( "Pad Two size invalid." );
        }
        try
        {
            speedOne = Integer.parseInt( jTextFieldSpeedOne.getText() );
            if( speedOne > 10 )
                speedOne = 10;

            canvas.setPad1Speed( speedOne );
        }
        catch( Exception e )
        {
            if( jTextFieldSpeedOne.getText() != null )
                System.out.println( "Pad One speed invalid." );
        }

        try
        {
            speedTwo = Integer.parseInt( jTextFieldSpeedTwo.getText() );
            if( speedTwo > 10 )
                speedTwo = 10;

            canvas.setPad2Speed( speedTwo );
        }
        catch( Exception e )
        {
            if( jTextFieldSpeedTwo.getText() != null )
                System.out.println( "Pad Two speed invalid." );
        }

        this.setVisible( false );
}//GEN-LAST:event_jButtonOKActionPerformed

